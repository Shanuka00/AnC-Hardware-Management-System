    // Input validation method
    private boolean validateInputs() {
        String supName = txtSupName.getText();
        String telNo = txtTelNo.getText();
        String email = txtEmail.getText();
        String addr = txtAddr.getText();

        if (supName.isEmpty() || telNo.isEmpty() || email.isEmpty() || addr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all the required fields.");
            return false; // invalid input values
        }

        // validate supplier name
        if (supName.length() < 3 || supName.length() > 50) {
            JOptionPane.showMessageDialog(this, "Supplier name should be between 3 and 50 characters long!");
            return false; // supplier name should be between 3 and 50 characters long
        }

        // validate telephone number
        if (!telNo.matches("^0[1-9][0-9]{8}$")) {
            JOptionPane.showMessageDialog(this, "Telephone number is invalid!");
            return false; // telephone number should only contain digits, spaces, and dashes
        }

        // validate email
        if (!email.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")) {
            JOptionPane.showMessageDialog(this, "Invalid email format!");
            return false; // invalid email format
        }

        // validate address
        if (addr.length() < 5 || addr.length() > 100) {
            JOptionPane.showMessageDialog(this, "Address should be between 5 and 100 characters long!");
            return false; // address should be between 5 and 100 characters long
        }

        return true; // all input values are valid
    }
    
