    private void updateSuppTbl(){
        
        try {
            
            //get date and add suppliers to table
            Date d = new Date();

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

            String dd = sdf.format(d);
            lblDate.setText(dd);
            
            //clean the table
            
            try{
                int j = tblSupp.getRowCount();
                DefaultTableModel dtm = (DefaultTableModel) tblSupp.getModel();

                for (int k=0; k<j; k++){
                    dtm.removeRow(0);
                }
            }
            catch(ArrayIndexOutOfBoundsException e){
                System.out.println(e.getMessage()); 
            }

            DBConn dbConn = new DBConn();
            Connection conn = dbConn.conn();
            
            Statement stm2 = (Statement) conn.createStatement();
            
            String sql = "SELECT * FROM supplier;";
            
            ResultSet rs = stm2.executeQuery(sql);
                
            while (rs.next()){
                String sID = String.valueOf(rs.getString("S_ID"));
                String sName = String.valueOf(rs.getString("S_NAME"));
                String telNo = String.valueOf(rs.getString("TEL_NO"));
                String email = String.valueOf(rs.getString("EMAIL"));
                String addr = String.valueOf(rs.getString("ADD_"));
                
                //string array for store data into table
                String tbData[] = {sID,sName,telNo,email,addr};
                DefaultTableModel tblModel = (DefaultTableModel) tblSupp.getModel();
                
                //add string array data into tblStock
                tblModel.addRow(tbData);
            }
        }
        catch (SQLException ex) {
            Logger.getLogger(CustomerFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
