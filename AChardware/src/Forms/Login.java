    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        // Login code
        try{
            // Open connection
            //Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/achardware","root","");
            //here AChardware is database name
            //root is username of mysql
            
            String username = txtUserName.getText();
            String password = txtPassword.getText();
            
            Statement stm = (Statement) con.createStatement();
            
            //mysql query to run
            String sql = "select * from employee where username='"+username+"' and password='"+password+"';";
            ResultSet rs = stm.executeQuery(sql);
            
            if (rs.next()){
                //if username and password is true than go to homepage
                String role = rs.getString("Role");
                if (role.equals("manager")){
                    dispose(); //close login page
                    ManagerFrame mpage = new ManagerFrame();
                    mpage.show();
                }
                else if (role.equals("cashier")){
                    dispose(); //close login page
                    CashierFrame cpage = new CashierFrame();
                    cpage.show();
                }   
            }
            else{
                //if username and password is wrong show message
                JOptionPane.showMessageDialog(this, "username or password is incorrect!");
                txtUserName.setText("");
                txtPassword.setText("");
            }
                con.close();
            }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

