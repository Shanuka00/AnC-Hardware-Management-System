    private boolean isValidDateChooser(JDateChooser dateChooser) {
        Date selectedDate = dateChooser.getDate();
        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Please select a date from the Date Chooser!");
            return false;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String dateStr = sdf.format(selectedDate);
        try {
            sdf.parse(dateStr);
            return true;
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Please select a valid date in the format yyyy-MM-dd.");
            return false;
        }
    }
    
    private void btnGenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenActionPerformed
        // TODO add your handling code here:
        
        try {
            // Validate the date chooser
            if (!isValidDateChooser(dteChooser)) {
                return;
            }

            // Clean the table
            int j = tblSales.getRowCount();
            DefaultTableModel dtm = (DefaultTableModel) tblSales.getModel();
            for (int k = 0; k < j; k++) {
                dtm.removeRow(0);
            }

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dte = sdf.format(dteChooser.getDate());
            boolean chk;

            DBConn dbConn = new DBConn();
            Connection conn = dbConn.conn();

            Statement stm2 = (Statement) conn.createStatement();
            String sql = "SELECT EXISTS(SELECT 1 FROM sales WHERE DATE_ = '"+dte+"') AS CHK;";
            ResultSet rs = stm2.executeQuery(sql);

            while (rs.next()) {
                chk = rs.getBoolean("CHK");
                if (chk) {
                    Statement stm = (Statement) conn.createStatement();
                    String sql2 = "SELECT * FROM SALES\n" + "WHERE DATE_ = '"+dte+"';";
                    ResultSet rs2 = stm.executeQuery(sql2);

                    while (rs2.next()) {
                        String bID = String.valueOf(rs2.getString("B_ID"));
                        String eID = String.valueOf(rs2.getString("E_ID"));
                        String disc = String.valueOf(rs2.getString("DISCOUNT"));
                        String totp = String.valueOf(rs2.getString("TOT_PRICE"));

                        //string array for store data into table
                        String tbData[] = {bID,eID,disc,totp};
                        DefaultTableModel tblModel = (DefaultTableModel) tblSales.getModel();
                        //add string array data into tblStock
                        tblModel.addRow(tbData); 
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No sale has been done on "+dte+"");
                    updateSalesTbl();
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGenActionPerformed

