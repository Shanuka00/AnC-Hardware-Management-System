    private void tblSalesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSalesMouseClicked
        // TODO add your handling code here:
        try{
            if (tblSales.getSelectedRowCount()==1){
                
                int row = tblSales.getSelectedRow();
                
                String bill_id = tblSales.getValueAt(row,0).toString();
                
                //clear bill table
                try{
                    int j = tblBill.getRowCount();
                    DefaultTableModel dtm = (DefaultTableModel) tblBill.getModel();

                    for (int k=0; k<j; k++){
                        dtm.removeRow(0);
                    }
                }
                catch(ArrayIndexOutOfBoundsException e){
                    System.out.println(e.getMessage()); 
                }
                
                //pass data to next table
                DBConn dbConn = new DBConn();
                Connection conn = dbConn.conn();
                        
                Statement stm = (Statement) conn.createStatement();

                String sql = "SELECT * FROM BILL WHERE B_ID = '"+bill_id+"';";

                ResultSet rs = stm.executeQuery(sql);
                    
                while (rs.next()){
                    
                    String iid = String.valueOf(rs.getString("ITEM_ID"));
                    String spr = String.valueOf(rs.getString("SELLING_PRICE"));
                    String dis = String.valueOf(rs.getString("DISCOUNT"));
                    String qty = String.valueOf(rs.getString("QUANTITY"));

                    //string array for store data into table
                    String tbData[] = {iid,spr,dis,qty};
                    DefaultTableModel tblModel = (DefaultTableModel) tblBill.getModel();

                    //add string array data into tblStock
                    tblModel.addRow(tbData); 
                }
                
            }
            else{
                if(tblSales.getRowCount()==0){
                    //if empty selected
                    JOptionPane.showMessageDialog(this,"Table selection is empty!");
                }
                else{
                    //if selected multiple rows
                    JOptionPane.showMessageDialog(this,"Please select a single row!");
                }
            }           
        }
        catch(HeadlessException e){
            System.out.println(e.getMessage());
        } catch (SQLException ex) {
            Logger.getLogger(Report.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblSalesMouseClicked

